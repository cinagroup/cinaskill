    if [ "$mtime_after" -gt "$mtime_before" ]; then
        log_success "Mtime tracking working (before: $mtime_before, after: $mtime_after)"
        TESTS_PASSED=$((TESTS_PASSED + 1))
    else
        log_error "Mtime tracking failed"
        TESTS_FAILED=$((TESTS_FAILED + 1))
    fi
    
    # Test 4.4: Lock File Mechanism
    log "4.4 Testing lock file mechanism..."
    
    cat > "$TEST_RESULTS_DIR/tmp/lock-test.js" << 'EOF'
const fs = require('fs');
const path = require('path');

const LOCK_FILE = path.join(process.argv[2], '.consolidate-lock');

function acquireLock(memoryDir) {
    const lockPath = path.join(memoryDir, '.consolidate-lock');
    
    try {
        // Check if lock exists
        if (fs.existsSync(lockPath)) {
            const lock = JSON.parse(fs.readFileSync(lockPath, 'utf-8'));
            const age = Date.now() - lock.timestamp;
            const STALE_MS = 3600000; // 1 hour
            
            // Check if lock is stale
            if (age >= STALE_MS) {
                // Lock is stale, overwrite
            } else {
                // Lock is still valid
                return { acquired: false, reason: 'Lock held by another process' };
            }
        }
        
        // Acquire lock
        fs.writeFileSync(lockPath, JSON.stringify({
            pid: process.pid,
            timestamp: Date.now()
        }));
        
        return { acquired: true, reason: 'Lock acquired' };
    } catch (e) {
        return { acquired: false, reason: e.message };
    }
}

function releaseLock() {
    try {
        fs.unlinkSync(LOCK_FILE);
        return { released: true };
    } catch (e) {
        return { released: false, error: e.message };
    }
}

// Test
const acquireResult = acquireLock(process.argv[2]);
console.log('Acquire:', JSON.stringify(acquireResult));

// Try to acquire again (should fail)
const secondAttempt = acquireLock(process.argv[2]);
console.log('Second attempt:', JSON.stringify(secondAttempt));

// Release
const releaseResult = releaseLock();
console.log('Release:', JSON.stringify(releaseResult));

console.log(JSON.stringify({
    passed: acquireResult.acquired && !secondAttempt.acquired && releaseResult.released
}));
EOF

    local lock_result=$(node "$TEST_RESULTS_DIR/tmp/lock-test.js" "$TEST_MEMORY_DIR" 2>&1)
    if echo "$lock_result" | grep -q '"passed":true'; then
        log_success "Lock file mechanism working"
        TESTS_PASSED=$((TESTS_PASSED + 1))
    else
        log_error "Lock file mechanism failed"
        if [ "$VERBOSE" = true ]; then
            log "  Output: $lock_result"
        fi
        TESTS_FAILED=$((TESTS_FAILED + 1))
    fi
    
    # Test 4.5: Session Memory File Persistence
    log "4.5 Testing session memory file persistence..."
    
    local session_file="$SESSION_MEMORY_DIR/test-session-$(date +%s).md"
    cat > "$session_file" << 'EOF'
# Integration Test Session

# Current State
Running integration tests for memory system.

# Task Specification
Test complete memory lifecycle including extraction, recall, compaction, and consolidation.

# Files and Functions
- test/integration-tests.sh - Main test script
- test/results/integration-report.md - Test results report

# Errors & Corrections
None so far.

# Learnings
- Circuit breaker prevents cascade failures
- Lock files prevent concurrent consolidation
EOF

    if [ -f "$session_file" ]; then
        local session_size=$(wc -c < "$session_file")
        log_success "Session memory file created ($session_size bytes)"
        TESTS_PASSED=$((TESTS_PASSED + 1))
        
        # Clean up session file
        rm -f "$session_file"
    else
        log_error "Session memory file creation failed"
        TESTS_FAILED=$((TESTS_FAILED + 1))
    fi
    
    local end_time=$(date +%s)
    local duration=$((end_time - start_time))
    log_test "State Persistence Complete - Duration: ${duration}s"
}

